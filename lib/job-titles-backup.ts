/**
 * Flattened array of all job titles for easy iteration
 * Deduplicated to ensure uniqueness
 */
export const ALL_JOB_TITLES = Array.from(
  new Set(Object.values(JOB_TITLES).flat())
).sort();

/**
 * Get job titles by category
 */
export function getJobTitlesByCategory(category: keyof typeof JOB_TITLES): readonly string[] {
  return JOB_TITLES[category];
}

/**
 * Search job titles by query string
 */
export function searchJobTitles(query: string): string[] {
  const lowerQuery = query.toLowerCase();
  return ALL_JOB_TITLES.filter(title => 
    title.toLowerCase().includes(lowerQuery)
  );
}
